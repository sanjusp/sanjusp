void main() {
  int temperature = 25;

  if (temperature > 30) {
    print("It's hot outside. Wear light clothes.");
  } else if (temperature > 20) {
    print("The weather is pleasant. Enjoy your day!");
  } else if (temperature > 10) {
    print("It's a bit chilly. You might want to bring a jacket.");
  } else {
    print("It's cold outside. Bundle up!");
  }
}
